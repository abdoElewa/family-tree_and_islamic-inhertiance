father(Father,Child) :- children(Father,Child) , gender(Father,male).
mother(Mother,Child) :- children(Mother,Child) , gender(Mother,female).

parent(Parent,Child) :- father(Parent,Child) ; mother(Parent,Child)  .
grandparents(X,Y):-parent(X,Z),parent(Z,Y).
grandmother(X,Y):-parent(X,Z),parent(Z,Y),gender(X,female).
grandfather(X,Y):-parent(X,Z),parent(Z,Y),gender(X,male).


uncle(X,Y):-brother(Z,X),parent(Z,Y),gender(X,male).

aunty(X,Y):-uncle(Z,Y),spouse(Z,X),gender(X,female) ;
            father(Z,Y) , sister(X,Z) ,gender(X,female) ;
            mother(Z,Y),sister(X,Z),gender(X,female).

nephew(X,Y):-siblings(Z,Y),son(X,Z),gender(X,male).
niece(X,Y):-siblings(Z,Y),daughter(X,Z),gender(X,female).
cousin(X,Y):-parent(Z,X),parent(W,Y),siblings(Z,W),\+brother(X,Y),\+sister(X,Y),X\=Y.

brother(X,Y):-father(Z,X),father(Z,Y),X\=Y,gender(X,male).
sister(X,Y):-father(Z,X),father(Z,Y),X\=Y,gender(X,female).
siblings(X,Y):-parent(Z,X),parent(Z,Y),X\=Y.
son(X,Y):-father(Y,X),gender(X,male) ; mother(Y,X),gender(X,male).
daughter(X,Y):-father(Y,X),gender(X,female) ; mother(Y,X),gender(X,female) .






