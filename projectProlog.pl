    person(Person, DateOfBirth, DateOfDeath),
    (

        nonvar(DateOfDeath) ->  % True   or False  ---- False
            calculate_age(DateOfBirth, DateOfDeath, Age),
            write('This person died aged  ') , writeln(Age), write(Person),write(' died on a '),writeln(DateOfDeath)
        ;
            calculate_age(DateOfBirth, FinalDate, Age)
    ).


calculate_age(date(Year, Month, Day), date(Year1, Month1, Day1), Age) :-
    (Month1 < Month ; (Month1 == Month, Day1 < Day)) ->
        Age is Year1 - Year - 1 ;
        Age is Year1 - Year.





